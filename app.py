def gradient_text(text_str, colors=None):
    """Cria texto com efeito gradiente."""
    if colors is None:
        colors = GRADIENT_COLORS
    t = Text()
    for i, char in enumerate(text_str):
        color = colors[i % len(colors)]
        t.append(char, style=Style(color=color, bold=True))
    return t

